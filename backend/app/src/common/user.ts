import { attribute, autoGeneratedHashKey, rangeKey, table } from "@aws/dynamodb-data-mapper-annotations";

@table("users")
export class User {
    @autoGeneratedHashKey()
    id: string;

    @rangeKey()
    createdAt: Date;

    @attribute()
    username: string;

    @attribute()
    password: string;

    @attribute()
    score: number;

    @attribute()
    state: UserState;
}

export enum UserState {
    CAN_GUESS,
    GUESSINg,
}
